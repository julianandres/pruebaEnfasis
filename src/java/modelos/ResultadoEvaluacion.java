package modelos;



/**
 *
 * @author lenovo
 */
public class ResultadoEvaluacion {
    
    private String codigo;
    private String codigoGrupo;
    private String codigoIntegrante;
    private String codigoActividad;
    private String codigoCompetencia;
    private String valorResultado;

    public String getCodigo() {
        return codigo;
    }

    public void setCodigo(String codigo) {
        this.codigo = codigo;
    }

    public String getCodigoGrupo() {
        return codigoGrupo;
    }

    public void setCodigoGrupo(String codigoGrupo) {
        this.codigoGrupo = codigoGrupo;
    }

    public String getCodigoIntegrante() {
        return codigoIntegrante;
    }

    public void setCodigoIntegrante(String codigoIntegrante) {
        this.codigoIntegrante = codigoIntegrante;
    }

    public String getCodigoActividad() {
        return codigoActividad;
    }

    public void setCodigoActividad(String codigoActividad) {
        this.codigoActividad = codigoActividad;
    }

    public String getCodigoCompetencia() {
        return codigoCompetencia;
    }

    public void setCodigoCompetencia(String codigoCompetencia) {
        this.codigoCompetencia = codigoCompetencia;
    }

    public String getValorResultado() {
        return valorResultado;
    }

    public void setValorResultado(String valorResultado) {
        this.valorResultado = valorResultado;
    }
    
}
